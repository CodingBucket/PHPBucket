 # Constructor
---------------------------
 --construct() this magic method is called constructor.
 When a class object is created this magic is called at this time.
 This method is always in the class although if we dont declare in the class.

---------------------------
 # Destructor:
---------------------------
 --destruct() this magic method is called destructor .
 When a class object is destroyed then this method is called.
 
